    println(io, "## SOL 200 — Design Optimization Results")
    println(io)
    opt = _report_safe_get(results, "optimization", Dict{String,Any}())
    route = _report_safe_get(results, "route_summary", Dict{String,Any}())

    println(io, "**Optimization summary**")
    println(io)
    println(io, "| Quantity | Value |")
    println(io, "|---|---:|")
    println(io, "| Objective         | `", _report_safe_get(opt, "objective", "?"), "` |")
    println(io, "| Iterations        | ", _report_safe_get(opt, "n_iter", 0), " |")
    println(io, "| Converged         | ", _report_safe_get(opt, "converged", false), " |")
    println(io, "| Termination       | ", _report_safe_get(opt, "termination_reason", ""), " |")
    println(io, "| Design variables  | ", _report_safe_get(opt, "design_variable_count", 0), " |")
    println(io, "| Initial mass      | ", _report_fmt_sci(_report_safe_get(opt, "mass_initial", 0.0)), " |")
    println(io, "| Target mass       | ", _report_fmt_sci(_report_safe_get(opt, "mass_target", 0.0)), " |")
    println(io, "| Final mass        | ", _report_fmt_sci(_report_safe_get(opt, "final_mass", 0.0)), " |")
    println(io, "| Mass ratio (final/initial) | ", _report_fmt_float(_report_safe_get(opt, "mass_ratio", 0.0); digits=4), " |")
    forward_sol = _report_safe_get(results, "forward_sol_type", 0)
    println(io, "| Forward SOL type  | ", forward_sol, " |")
    for (k, v) in route
        println(io, "| route: ", k, " | ", v, " |")
    end
    println(io)

    history = _report_safe_get(opt, "history", [])
    if history isa AbstractVector && !isempty(history)
        println(io, "**Objective history** (", length(history), " point", length(history) == 1 ? "" : "s", ")")
        println(io)
        println(io, "| Iter | Objective |")
        println(io, "|---:|---:|")
        n = length(history)
        show_idx = if n <= 20
            collect(1:n)
        else
            vcat(1:6, [round(Int, i) for i in range(7, stop=n-6, length=8)], n-5:n) |> unique |> sort
        end
        for (i, row) in enumerate(history)
            i in show_idx || continue
            obj = row isa AbstractDict ? _report_safe_get(row, "objective", row) : row
            println(io, "| ", i, " | ", _report_fmt_sci(Float64(obj)), " |")
        end
        println(io)
    end

    forward_results = _report_safe_get(results, "forward_results", nothing)
    if forward_results isa AbstractDict
        fs_type = Int(_report_safe_get(forward_results, "sol_type", 0))
        println(io, "---\n\n## Forward-run results at final design (SOL ", fs_type, ")")
        println(io)
        if fs_type == 101
            _report_sol101_section(io, forward_results)
        elseif fs_type == 103
            _report_sol103_section(io, forward_results)
        elseif fs_type == 105
            _report_sol105_section(io, forward_results)
        elseif fs_type == 106
            _report_sol106_section(io, forward_results)
        else
            println(io, "_No tailored section for this forward SOL type._\n")
        end
    end
end
